package ruv.http;


import ruv.BlockchainProcessor;
import ruv.BlockchainTest;
import ruv.Helper;
import ruv.Ruv;
import org.junit.AfterClass;
import org.junit.Assert;
import org.junit.BeforeClass;

public abstract class AbstractHttpApiSuite {
    @BeforeClass
    public static void init() {
        BlockchainTest.initRuv();
        Ruv.getTransactionProcessor().clearUnconfirmedTransactions();
        Ruv.getBlockchainProcessor().addListener(new Helper.BlockListener(), BlockchainProcessor.Event.BLOCK_GENERATED);
        Assert.assertEquals(0, Helper.getCount("unconfirmed_transaction"));
    }

    @AfterClass
    public static void shutdown() {
        Assert.assertEquals(0, Helper.getCount("unconfirmed_transaction"));
        Ruv.shutdown();
    }
}
